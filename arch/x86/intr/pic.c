#include "pic.h"

#include <arch/x86/io.h>

void pic_init(void) {
	pic_remap();
}

void pic_remap(void) {
	io_put8(0x20, 0x11);
	io_put8(0xA0, 0x11);
	io_put8(0x21, 0x20);
	io_put8(0xA1, 0x28);
	io_put8(0x21, 0x04);
	io_put8(0xA1, 0x02);
	io_put8(0x21, 0x01);
	io_put8(0xA1, 0x01);
	io_put8(0x21, 0x00);
	io_put8(0xA1, 0x00);
}
